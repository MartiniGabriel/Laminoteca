package Aplicacao;

public class Maleta {

	String codMaleta;
	String descMaleta;
	String qtdPosicoes;
	String codPosicao;
	String statusPosicao;
	
	public String getCodMaleta() {
		return codMaleta;
	}
	public void setCodMaleta(String codMaleta) {
		this.codMaleta = codMaleta;
	}
	public String getDescMaleta() {
		return descMaleta;
	}
	public void setDescMaleta(String descMaleta) {
		this.descMaleta = descMaleta;
	}
	public String getQtdPosicoes() {
		return qtdPosicoes;
	}
	public void setQtdPosicoes(String qtdPosicoes) {
		this.qtdPosicoes = qtdPosicoes;
	}
	public String getCodPosicao() {
		return codPosicao;
	}
	public void setCodPosicao(String codPosicao) {
		this.codPosicao = codPosicao;
	}
	public String getStatusPosicao() {
		return statusPosicao;
	}
	public void setStatusPosicao(String statusPosicao) {
		this.statusPosicao = statusPosicao;
	}
	
}
