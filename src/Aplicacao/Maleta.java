package Aplicacao;

public class Maleta {

	String codMaleta;
	String descMaleta;
	String qtdPosicoes;
	
	public String getCodMaleta() {
		return codMaleta;
	}
	public void setCodMaleta(String codMaleta) {
		this.codMaleta = codMaleta;
	}
	public String getDescMaleta() {
		return descMaleta;
	}
	public void setDescMaleta(String descMaleta) {
		this.descMaleta = descMaleta;
	}
	public String getQtdPosicoes() {
		return qtdPosicoes;
	}
	public void setQtdPosicoes(String qtdPosicoes) {
		this.qtdPosicoes = qtdPosicoes;
	}
	
}
