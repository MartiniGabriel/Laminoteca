package Aplicacao;

public class Lamina {
	
	String id;
	String codigoTubo;
	String codSetor;
	String descSetor;
	String codMaterial;
	String descMaterial;
	String descDiagnostico;
	String dataCadastro;
	String codMaleta;
	String codPosicao;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCodigoTubo() {
		return codigoTubo;
	}
	public void setCodigoTubo(String codigoTubo) {
		this.codigoTubo = codigoTubo;
	}
	public String getCodSetor() {
		return codSetor;
	}
	public void setCodSetor(String codSetor) {
		this.codSetor = codSetor;
	}
	public String getDescSetor() {
		return descSetor;
	}
	public void setDescSetor(String descSetor) {
		this.descSetor = descSetor;
	}
	public String getCodMaterial() {
		return codMaterial;
	}
	public void setCodMaterial(String codMaterial) {
		this.codMaterial = codMaterial;
	}
	public String getDescMaterial() {
		return descMaterial;
	}
	public void setDescMaterial(String descMaterial) {
		this.descMaterial = descMaterial;
	}
	public String getDescDiagnostico() {
		return descDiagnostico;
	}
	public void setDescDiagnostico(String descDiagnostico) {
		this.descDiagnostico = descDiagnostico;
	}
	public String getDataCadastro() {
		return dataCadastro;
	}
	public void setDataCadastro(String dataCadastro) {
		this.dataCadastro = dataCadastro;
	}
	public String getCodMaleta() {
		return codMaleta;
	}
	public void setCodMaleta(String codMaleta) {
		this.codMaleta = codMaleta;
	}
	public String getCodPosicao() {
		return codPosicao;
	}
	public void setCodPosicao(String codPosicao) {
		this.codPosicao = codPosicao;
	}

}
